package io.github.rainblooding._3D.base;


public class _3DPoint {

    private float x;
    private float y;
    private float z;


    public _3DPoint(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }


    public float getX() {
        return x;
    }


    public float getY() {
        return y;
    }


    public float getZ() {
        return z;
    }

}
